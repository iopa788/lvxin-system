package com.farsunset.lvxin.repository;

import java.util.List;

import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Transactional;

import com.farsunset.lvxin.model.SNSShake;

@Repository
public interface SNSShakeRepository extends JpaRepository<SNSShake, String> {

	@Query("from SNSShake where account != ?1 and timestamp > ?2 order by RAND()")
	List<SNSShake> getRandomOne(String account, long timestamp, Pageable pageable);

	@Transactional
	@Modifying
	@Query("delete SNSShake   where timestamp <=?1")
	void deleteObsoleted(long timestamp);
}
